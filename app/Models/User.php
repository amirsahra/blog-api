 * @method static \Illuminate\Database\Eloquent\Builder|User wherePhone($value)
 * @method static \Illuminate\Database\Eloquent\Builder|User whereRememberToken($value)
 * @method static \Illuminate\Database\Eloquent\Builder|User whereStatus($value)
 * @method static \Illuminate\Database\Eloquent\Builder|User whereType($value)
 * @method static \Illuminate\Database\Eloquent\Builder|User whereUpdatedAt($value)
 * @mixin \Eloquent
 */
class User extends Authenticatable
{
    use HasApiTokens, HasFactory, Notifiable;

    /**
     * The attributes that are mass assignable.
     *
     * @var array<int, string>
     */
    protected $fillable = [
        'first_name', 'last_name', 'gender', 'type', 'status', 'avatar', 'phone', 'email', 'password',
    ];

    /**
     * The attributes that should be hidden for serialization.
     *
     * @var array<int, string>
     */
    protected $hidden = [
        'password',
        'remember_token',
    ];

    /**
     * The attributes that should be cast.
     *
     * @var array<string, string>
     */
    protected $casts = [
        'email_verified_at' => 'datetime',
    ];

    public function newUser(Request $request)
    {
        $avatarPath = null;
        if ($request->has('avatar')) {
            $avatarPath = Carbon::now()->microsecond . '.' . $request->avatar->extension();
            $request->avatar->storeAs('images/avatars/', $avatarPath, 'public');
        }

        return $this->query()->create([
            'first_name' => $request->get('first_name'),
            'last_name' => $request->get('last_name'),
            'gender' => $request->get('gender') ?? 'male',
            'status' => $request->get('status') ?? 'active',
            'type' => $request->get('type') ?? 'member',
            'phone' => $request->get('phone'),
            'email' => $request->get('email'),
            'avatar' => $avatarPath,
            'password' => Hash::make($request->get('password')),
        ]);

    }
}
